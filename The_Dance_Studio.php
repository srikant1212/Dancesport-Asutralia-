  <td align="left" valign="top" class="sohotext" width="100%">   	<p> </p>
	<p style="text-align: center;"><span style="color: #333399; font-family: 'comic sans ms', sand; font-size: xx-large;">Let's Dance Studio at Newmarket</span><strong><span style="color: #333399; font-family: comic sans ms,sand; font-size: x-large;"><br></span></strong></p>
	<p> </p>
	<p><span style="font-family: Verdana; font-size: 12pt;">Trisha and studio staff have one thing in common, their love of dancing and they enjoy sharing that love of dance at the Let's Dance Studio.  Trisha and the staff look forward to meeting you soon, so please </span><strong style="font-family: Verdana; font-size: 12pt;">Contact</strong><span style="font-family: Verdana; font-size: 12pt;"> Trisha Mob 0412 308 641 or </span><span style="font-family: Verdana; font-size: 12pt;">letsdancballroomstudio</span><span style="font-size: medium; font-family: verdana, geneva;">@outlook.com</span></p>
	<p><strong style="font-family: verdana, geneva; font-size: medium;">Cost: </strong><span style="font-family: verdana, geneva; font-size: medium;">The <span style="color: #ff0000;">cost is $12 per person each night</span>.  There are no contracts, no advance payments just come along on a Tuesday night and pay your $12 and have fun learning a new dance and/or practicing dances you have learnt previously.</span></p>
	<p><strong style="font-family: verdana, geneva; font-size: medium;">Couple Dancers:  </strong><span style="font-family: verdana, geneva; font-size: medium;">You do not need to bring a partner, but if you are attending with a partner you are welcome to dance together all night if you wish.  We do recommend that it is easier to learn if you occasionly dance with a staff member but the <span style="color: #ff0000;"><em>choice to change partners is your own.</em></span></span></p>
	<p><span style="font-family: verdana, geneva; font-size: medium;"><strong>Dances Taught: </strong>You will learn two dances each night and we give you a small script for each dance taught.  This can come in very handy if you want to practice at home or just have some help in remembering the sequence of the steps for a favourite dance.  You will learn a <a href="http://www.letsdanceballroom.info/Dances_taught_at_our_Studio.php">lot of dances</a> over a year so even the most practiced dancer can be seen referring to their script.</span></p>
	<p><span style="font-family: verdana, geneva; font-size: medium;"><strong>Dance Partner: </strong>When attending a social night at our studio, <span style="color: #ff0000;">you don't have to come with a dance partner.</span> The social night is designed so that you learn some dance steps AND meet new people.  Part of the fun of dancing is figuring out how to dance not only the steps you are taught, but also how to dance them correctly with a partner.  Although we can't guarantee a partner for every dance, we have staff to assist and singles and staff are asked to change partners after each practice dance so you will not sit out for more than one dance.</span></p>
	<p><span style="font-family: verdana, geneva; font-size: medium;"><strong>Dance Shoes: </strong>You can dance in street shoes so long as they are comfortable, do not have rubber soles. If you want to <a href="Tips_for_Choosing_Dance_Shoes.php">buy shoes</a> they can vary in price from approx $60 to $250.  That is a huge difference I know but for most dancers the $60 to $80 range is just perfect.  They come in lots of pretty colours, except for the guys of course (just black or brown).  We have included a few of the <a href="http://www.letsdanceballroom.info/Where_can_I_buy_Shoes.php">ballroom dance shoe suppliers</a> but remember to ring and ask about their price and range so you are not disappointed. "no thongs allowed"</span></p>
	<p><span style="font-family: verdana, geneva; font-size: medium;"><strong>Dress Code: </strong>We recommend comfortable, casual clothing.  If you would like more detailed information we have put together some information for <a href="http://www.letsdanceballroom.info/What_to_Wear_-_Men.php">men</a> and <a href="http://www.letsdanceballroom.info/What_to_Wear_-_Women.php">women</a>.</span></p>
	<p><strong style="font-family: verdana, geneva; font-size: medium;">First Timers: </strong><span style="font-family: verdana, geneva; font-size: medium;">That's OK  In our Social Classes everything is taught to you.   There are lessons for </span><span style="color: #ff0000; font-family: verdana, geneva; font-size: medium;">beginners, intermediate and advanced </span><span style="font-family: verdana, geneva; font-size: medium;">dancers. In each class you will be taught the basic steps of a particular dance. After each class there will be time to practice the steps that you have been taught with staff members and other class attendees.</span></p>
	<p><span style="font-family: verdana, geneva; font-size: medium;"><strong>How long does it take to learn to dance? </strong>Learning to dance is a lot like learning tennis. If you want to volley with a friend, you take a few lessons. If you want to play competitive tennis you spend your life learning. It's the same with dance. If you want to be able to get on a dance floor for an evening and have an enjoyable time dancing with fun and useful basic steps, then a basics will get you started. The more you want to learn and the more proficient you want to become, the more you need to study and practice. Â Most of our dancers just want to be able to dance enough dances to go out on a Saturday night to a dance, so when you learn about half the <a href="http://www.letsdanceballroom.info/Dances_taught_at_our_Studio.php">dances on a program</a> most people start going to one of the <a href="http://www.letsdanceballroom.info/Weekend_Dances.php">weekend dances</a> around Brisbane, usually about 12 months for most people.</span></p>
	<p><span style="font-family: verdana, geneva; font-size: medium;"><strong>Refreshments: </strong>There is always cold water, tea, coffee and biscuits available and once a month we have a <em>supper night </em>which everyone enjoys.</span></p>
	<p><strong style="font-family: verdana, geneva; font-size: medium;">Supper night: </strong><span style="font-family: verdana, geneva; font-size: medium;">The </span><span style="font-family: verdana, geneva; font-size: medium;">first Tuesday of every month </span><span style="font-family: verdana, geneva; font-size: medium;">so you will have a chance to meet everyone and socialise, please bring along a plate to share.</span></p>
	<p><span style="font-family: verdana, geneva; font-size: medium;"><strong>Tuesday Night Format: </strong> We have  approx. 17 assistants who volunteer their time and dance all the practice dances with ladies on the "special dance chairs row" and swap partners after each dance. We teach a new dance each week and revise the dance taught the week before each week, plus there are a few fun progressives.  I have put a <a href="http://www.letsdanceballroom.info/Tuesday_Night_Format.php">sample nights program</a> together so you can see what happens.</span></p>
	<p><span style="font-family: verdana, geneva; font-size: medium;"><strong>Tuesday Night Program:  </strong>An email is sent out each week advising what dances will be taught the following week.  To receive this email put your name on the email list at the door after your first night at Lets Dance. </span></p>
	<p><strong style="font-family: verdana, geneva; font-size: medium;">Weekend Dances: </strong><span style="font-family: verdana, geneva; font-size: medium;">We recommend that you take the first year to just learn, as there are about 60 dances on a Saturday night </span><a style="font-family: verdana, geneva; font-size: medium;" href="http://www.letsdanceballroom.info/Dances_taught_at_our_Studio.php">dance program</a><span style="font-family: verdana, geneva; font-size: medium;">, but you will be welcomed at any of the </span><span style="font-family: verdana, geneva; font-size: medium;">local dances</span> <span style="font-family: verdana, geneva; font-size: medium;">- most are run by friends of ours so we have listed a few for your convience.</span></p>
	<p><span style="font-family: verdana, geneva; font-size: medium;">More questions, </span><span style="font-family: verdana, geneva; font-size: medium;"><strong>Contact</strong> Trisha  0412 308 641 or letsdancballroomstudio@outlook.com </span><span style="font-family: verdana, geneva; font-size: medium;"> or </span><span style="font-family: verdana, geneva; font-size: medium;">try our </span><a style="font-family: verdana, geneva; font-size: medium;" href="http://www.letsdanceballroom.info/FAQs.php"><strong>FAQ</strong> </a><span style="font-family: verdana, geneva; font-size: medium;">page.</span></p>
	

